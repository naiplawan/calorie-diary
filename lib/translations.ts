// Language context and hooks
let currentLanguage: Language = 'th';

export const setLanguage = (lang: Language) => {
  currentLanguage = lang;
  if (typeof window !== 'undefined') {
    localStorage.setItem('preferred-language', lang);
  }
};

export const getLanguage = (): Language => {
  if (typeof window !== 'undefined') {
    const stored = localStorage.getItem('preferred-language') as Language;
    if (stored && (stored === 'en' || stored === 'th')) {
      return stored;
    }
  }
  return currentLanguage;
};

export const getTranslations = (lang?: Language) => {
  const selectedLang = lang || getLanguage();
  return selectedLang === 'en' ? en : th;
};

export const t = (key: string, lang?: Language): string => {
  const translations = getTranslations(lang);
  return (translations as any)[key] || key;
};

